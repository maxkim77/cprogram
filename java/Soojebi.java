public class Soojebi {
    int a = 5;
    void fn(){
        a = a + 3;
    }
    public static void main(String[] args) {
        Soojebi s = new Soojebi();
        s.a = s.a + 5;
        s.fn();
        System.out.println(s.a);
    }
}

